ፀሎት ሃይማኖት ሓዋርያት ዝደረስዎ ኣኣምን ብእግዚኣብሔር ኣብ፣ ኵሉ ዝኽእል፣ ሰማይን ምድርን ዝፈጠረ። ብኢየሱስ ክርስቶስ እውን ሓደ ወዱ ጐይታና፣ ንሱ ብመንፈስ ቅዱስ ተፀንሰ፤ ካብ ድንግል ማርያም ተወልደ። ሓመመ፣ ብትሕቲ ስልጣኑ ንጲላጦስ ጴንጤናዊ፣ ተሰቕለ፣ ሞተ፣ ተቐብረ። ናብ ሲኦል ወረደ፣ ብሱልሳይ መዓልቲ እንካብ ምውታን ተንስአ። ናብ ሰማይ ወፀ፣ ኣብ የማን ኪለው ዝኽእል እግዚኣብሔር ኣብ ተቐመጠ። ደኃር እውን ንዘለዉን ንዝሞቱን ክፈርድ ይመፅእ። ኣኣምን ብመንፈስ ቅዱስ፣ ቅድስቲ ቤተክርስትያን ካቶሊካዊት፣ ናይ ቅዱሳን ሱታፌ፣ ናይ ኃጥኣት ስርየት፣ ናይ ምውታን ትንሱኤ፣ ናይ ዘለዓለም ሕይወት። ኣሜን።
